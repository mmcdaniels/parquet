defmodule Parquet.Idl.PageType do
  use Ecto.Type

  @impl Ecto.Type
  def type, do: :string

  @impl Ecto.Type
  def cast(value) when is_integer(value) do
    value =
      case value do
        0 -> :DATA_PAGE
        1 -> :INDEX_PAGE
        2 -> :DICTIONARY_PAGE
        3 -> :DATA_PAGE_V2
      end

    {:ok, value}
  end

  @impl Ecto.Type
  def cast(_), do: :error

  @impl Ecto.Type
  def dump(_), do: :error

  @impl Ecto.Type
  def load(_), do: :error
end
